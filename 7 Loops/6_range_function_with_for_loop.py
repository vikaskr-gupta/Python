for i in range(8):
    print(i)       # It printed 0 to 7(By default it set to be start to 0)
# ********************************************


for i in range(1, 8):
    print(i)       # It printed 1 to 7(strat, stop, step-size). By default step-size is set to be 1
#*********************************************


for i in range(10, 21):
    print(i)       # It printed 10 to 20
#*********************************************


for i in range(1, 8, 2):
    print(i)       # It printed 1 to 8 with the difference 2(start, stop, Step-size)
#*********************************************


for i in range(1, 20, 3):
    print(i)       # It printed 1 to 20 with the difference 3(start, stop, Step-size)
